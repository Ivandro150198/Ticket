    <h2>1. Objeto</h2>
    <p>A EventTicket-GB intermedia a venda de bilhetes para eventos em Portugal e Guiné-Bissau. Ao criar conta ou concluir uma compra, aceita estes termos.</p>

    <h2>2. Bilhetes e informação obrigatória</h2>
    <p>Os bilhetes digitais incluem, nos termos do Decreto-Lei n.º 23/2014 e alterações: identificação do promotor e NIF, nome do espetáculo, preço, data e hora, local/recinto, classificação etária e, quando aplicável, numeração de lugares ou indicação de lotação. O bilhete é <strong>nominativo</strong> e associado a um <strong>código QR único</strong>.</p>

    <h2>3. Proibição de revenda abusiva e especulação</h2>
    <p>É <strong>proibida a revenda não autorizada</strong> de bilhetes, nomeadamente a preços superiores ao valor original de venda ao público, bem como a falsificação, reprodução ou cedência fraudulenta de códigos QR. A EventTicket-GB e o promotor podem recusar entrada, cancelar bilhetes emitidos em fraude e colaborar com autoridades (incluindo a ASAE, no âmbito das suas competências).</p>

    <h2>4. Validação e uso único</h2>
    <p>Cada bilhete só pode ser validado uma vez na entrada. Após leitura bem-sucedida, o código fica marcado como utilizado.</p>

    <h2>5. Pagamentos</h2>
    <p>Em Portugal disponibilizamos Multibanco, MB WAY e cartão (e métodos de demonstração nesta versão). Em Guiné-Bissau: Orange Money, transferência UEMOA e cartão. A confirmação da compra depende da efetiva liquidação do pagamento.</p>

    <h2>6. Cancelamento de espetáculos e reembolso</h2>
    <p>Se o promotor cancelar o espetáculo, o consumidor tem direito ao <strong>reembolso do valor do bilhete</strong>, nos termos da legislação aplicável. Pedidos de reembolso por iniciativa do cliente (fora de cancelamento) seguem as regras do evento e a análise da EventTicket-GB / promotor. Contacte o suporte através do <a href="<?= base_url('contacto') ?>">formulário de contacto</a>.</p>

    <h2>7. Faturação</h2>
    <p>Em ambiente de produção, a emissão de faturas em Portugal deve utilizar software certificado pela Autoridade Tributária e Aduaneira e cumprir a comunicação de séries/faturas (SAF-T ou webservice). Nesta demonstração é gerado um comprovativo/fatura modelo sem valor fiscal certificado.</p>

    <h2>8. Reclamações e RAL</h2>
    <p>Disponibilizamos acesso ao <a href="<?= base_url('livro-de-reclamacoes') ?>">Livro de Reclamações Eletrónico</a>. Em litígios de consumo, consulte a informação sobre <a href="<?= base_url('resolucao-litigios') ?>">Resolução Alternativa de Litígios (RAL)</a>.</p>

    <h2>9. Limitação</h2>
    <p>A EventTicket-GB não se responsabiliza por alterações de programa comunicadas pelo promotor, atrasos de acesso ao recinto ou incumprimentos imputáveis a terceiros, sem prejuízo dos direitos imperativos do consumidor.</p>

